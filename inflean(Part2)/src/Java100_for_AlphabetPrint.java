
public class Java100_for_AlphabetPrint {

	public static void main(String[] args) {
		
		//알파벳 26개 문자를 대문자로 출력하는 반복문을 구현하시오..
		
		//for 반복문 --> A(65)~Z(90)
		
		for(int i = 65; i<91; i++) {
			System.out.print(i + " ");
	
		}
		
		System.out.println();
		
		
		for(int i = 65; i<91; i++) {
			System.out.print((char)i + " ");
	
		}
		
	
	}

}
